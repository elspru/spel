 YV="\033[1;33m\033[44m"     	#Vowel BG
 CV="\033[0;36m\033[41m"
 BV="\033[0;34m\033[43m"    	#Blue
#Black       0;30    
#Light Gray  0;37    
#Dark Gray     1;30
#White         1;37

out="$Yellow"
echo -e "${Y}s${Ch}l${R}a${N} ${R}m${B}u$N${Pi}n${C}t${None}"
